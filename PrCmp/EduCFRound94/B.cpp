void resuelveCaso() {
	int p, f, cs, cw, s, w; cin >> p >> f >> cs >> cw >> s >> w;

	if(s > w) {
		swap( s,  w);
		swap(cs, cw);
	}

	
}

int main() {
	int T; cin >> T;
	for (int i = 1; i <= T; i++) {
		//cout << "Case #" << i << ": ";
		resuelveCaso();
	}
	return 0;
}


